    private void verificarLogin() {
        String apelido = jTxtApelido.getText();
        String senha = new String(jPswSenha.getText());
        
        // Validações
        if (apelido.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite um apelido valido");
            jTxtApelido.requestFocus();
            
        }
        
        if (senha.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite uma senha valido");
            jPswSenha.requestFocus();
            
        }
        
        // Autenticar no banco
        UsuariosDAO usuariosDAO = new UsuariosDAO();
        usuarioLogado = usuariosDAO.login(apelido, senha);
        
        if (usuarioLogado != null) {
            JOptionPane.showMessageDialog(this, "Login realizado com sucesso!");
            JFrmPrincipal_GME jFrmPrincipal = new JFrmPrincipal_GME();
            jFrmPrincipal.setVisible(true); 
        } else {
            JOptionPane.showMessageDialog(this, 
                "Login errado!", 
                "Erro de login!", 
                JOptionPane.ERROR_MESSAGE);
            jPswSenha.setText("");
            jPswSenha.requestFocus();
        }
    }

