    private void jBtnConsulta_GMEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConsulta_GMEActionPerformed
        // TODO add your handling code here:
        if (jTxtNome_Empresa_GME.getText().isEmpty() == false && jTxtCliente_GME.getText().isEmpty() == false) {

            List lista = (List) vendaDAO.listEmpresaCliente(jTxtNome_Empresa_GME.getText(),
                    jTxtCliente_GME.getText());
            ControllerConsultasVenda.setList(lista);
        } else if (jTxtNome_Empresa_GME.getText().isEmpty() == false) {
            List lista = (List) vendaDAO.listEmpresa(jTxtNome_Empresa_GME.getText());
            ControllerConsultasVenda.setList(lista);
        } else if (jTxtCliente_GME.getText().isEmpty() == false) {
            List lista = (List) vendaDAO.listCliente(jTxtCliente_GME.getText());
            ControllerConsultasVenda.setList(lista);
        } else {
            List lista = (List) vendaDAO.listAll();
            ControllerConsultasVenda.setList(lista);
        }

    }//GEN-LAST:event_jBtnConsulta_GMEActionPerformed

    private void jBtnImprimir_GMEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnImprimir_GMEActionPerformed
        // TODO add your handling code here:
        try {
            boolean completo = jTable1.print(
                    JTable.PrintMode.FIT_WIDTH,
                    new MessageFormat("Relatório de Consultas de Cliente"),
                    new MessageFormat("Página {0}")
            );

            if (completo) {

                JOptionPane.showMessageDialog(this, "Impressão concluída!");
            } else {
                JOptionPane.showMessageDialog(this, "Impressão cancelada.");
            }

        } catch (PrinterException ex) {
            JOptionPane.showMessageDialog(this,
                    "Erro ao imprimir: " + ex.getMessage(),
                    "Erro",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_jBtnImprimir_GMEActionPerformed

